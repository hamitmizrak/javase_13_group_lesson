package com.ecodation.a15.oop;

public class _1PojoBeanTuto {
	// MVC ==> Model yani bean yapıdır.
	// bean: çekirdek :POJO : sadeec özellikler
	
	// nesne özellkleri
	private String kalemRengi;
	private double kalemFiyati;
	
	// getter and setter
	public String getKalemRengi() {
		return kalemRengi;
	}
	
	public void setKalemRengi(String kalemRengi) {
		this.kalemRengi = kalemRengi;
	}
	
	public double getKalemFiyati() {
		return kalemFiyati;
	}
	
	public void setKalemFiyati(double kalemFiyati) {
		this.kalemFiyati = kalemFiyati;
	}
	
}

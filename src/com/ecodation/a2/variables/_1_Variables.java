package com.ecodation.a2.variables;

public class _1_Variables {
	
	public static void main(String[] args) {
		// UNUTTTMAAAAAAAAAAAAAAAAAA:
		// Javada eğer Main metodu yazmazsanız kodlarınızı çalıştıramazsınız
		// javada o classın içinde sadece 1 tane public class vardır.
		
		// değişkenlerimiz nedir ?
		// değişken adları ==> java rules
		/*
		 * Türkçe karakter kullanmayalım: üğşçö
		 * int sayiüğşçö = 55;
		 * .
		 * sayı ile başlamayalım
		 * int 99sayi = 55;
		 * .
		 * değişken sonuna sayı yazabilirsiniz
		 * int sayi99 = 55;
		 * .
		 * değişken underscore(Alt çizgi ile başlayabiliriz
		 * int _sayi = 55;
		 * .
		 * değişken dolar ile başlayabiliriz.
		 * int $sayi = 55;
		 */
		int $sayi = 55;
		System.out.println("sayım:" + $sayi);
		
		// f-L
		// float ==> f
		// long ==> L
		
		// tamsayılar ==> b<s<i<l
		// Ctrl+Space ==> auto complete
		byte b1 = 12;
		System.out.println(b1);
		
		short s1 = 55;
		System.out.println(s1);
		
		int i1 = 99;
		System.out.println(i1);
		
		long l1 = 666L;
		System.out.println(l1);
		
		// virgüllü sayılar
		// matemattikteki virgül javada nokta demektir.
		float f2 = 14.55f;
		System.out.println(f2);
		
		double d2 = 555.66;
		System.out.println(d2);
		
		// boolean: doğru yanlışı gösteri
		boolean b3 = true;
		System.out.println(b3);
		
		// char : tek simge gösterir
		char c4 = 'a';
		System.out.println(c4);
		
		char c5 = '\u4563';
		System.out.println(c5);
		
	}
	
}

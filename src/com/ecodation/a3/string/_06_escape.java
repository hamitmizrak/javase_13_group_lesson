package com.ecodation.a3.string;

public class _06_escape {
	
	public static void main(String[] args) {
		// "" ''
		// escape karakter(Çıkış karakter)
		System.out.println("1.adım\"");
		System.out.println("2.adım\'");
		System.out.println("3.adım\n"); // n=yeni satıra geç
		System.out.println("4.adım\tyeni"); // t=tab karakter boşluk bırak anlamına geliyor.
	}
	
}
